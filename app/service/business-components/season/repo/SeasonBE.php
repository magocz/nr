<?php


class SeasonBE
{
    public $id;
    public $userId;
    public $active;
    public $name;
    public $fields = array();

    /**
     * SeasonBE constructor.
     */
    public function __construct($seasonDB)
    {
        $this->id = $seasonDB['ID'];
        $this->userId = $seasonDB['USER_ID'];
        $this->name = $seasonDB['NAME'];
        $this->active = 0;
    }

    public function addField($field)
    {
        array_push($this->fields, $field);
    }

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getUserId()
    {
        return $this->userId;
    }

    /**
     * @param mixed $userId
     */
    public function setUserId($userId)
    {
        $this->userId = $userId;
    }

    /**
     * @return mixed
     */
    public function getActive()
    {
        return $this->active;
    }

    /**
     * @param mixed $active
     */
    public function setActive($active)
    {
        $this->active = $active;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param mixed $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return array
     */
    public function getFields()
    {
        return $this->fields;
    }

    /**
     * @param array $fields
     */
    public function setFields($fields)
    {
        $this->fields = $fields;
    }


}